function Colours.getColour(hue, index, alpha)
    if index < 1 or index > 26 then return {.7,.7,.7} end
    if alpha == nil then alpha = 1 end
    if hue == 1 then -- vivid
        return {vividColours[index][1]/255, vividColours[index][2]/255,vividColours[index][3]/255, alpha}
    elseif hue == 2 then -- light
        return {lightColours[index][1]/255, lightColours[index][2]/255,lightColours[index][3]/255, alpha}
    elseif hue == 3 then -- pale
        return {paleColours[index][1]/255, paleColours[index][2]/255,paleColours[index][3]/255, alpha}
    end
end

